def number_to_month(month):
    if month == 1:
        return print("enero")
    if month == 2:
        return print("febrero")
    if month == 3:
        return print("marzo")
    if month == 4:
        return print("abril")
    if month == 5:
        return print("mayo")
    if month == 6:
        return print("junio")
    if month == 7:
        return print("julio")
    if month == 8:
        return print("agosto")
    if month == 9:
        return print("septiembre")
    if month == 10:
        return print("octubre")
    if month == 11:
        return print("noviembre")
    if month == 12:
        return print("diciembre")
    else:
        return print("error")


number_to_month(13)